DECLARE
BANK_BAL NUMBER:=15000;
AMT NUMBER:=&AMT;
REMAINING NUMBER;

BEGIN
 IF BANK_BAL > AMT
  THEN
  REMAINING:=BANK_BAL-AMT;
  DBMS_OUTPUT.PUT_LINE('AVAILABLE  --->>>>'||REMAINING);
 ELSE
   DBMS_OUTPUT.PUT_LINE('INSUFFICIENT');
 END IF;
END;
